public class unaryop {
    public static void main(String[] args) {
       //post increment
        int a=10;
        int b=++a;
      //pre increment
        int x=10;
        int y=x++;
        System.out.println(a);
        System.out.println(b);
        System.out.println(x);
        System.out.println(y);
 
    }

}
